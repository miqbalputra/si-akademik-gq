$files = array_merge(
    glob(__DIR__ . '/../app/Filament/Resources/*/Schemas/*.php'),
    glob(__DIR__ . '/../app/Filament/Resources/*/Tables/*.php')
);

foreach ($files as $file) {
    $content = file_get_contents($file);
    $lines = explode("\n", $content);
    $newLines = [];
    $modified = false;

    for ($i = 0; $i < count($lines); $i++) {
        $line = $lines[$i];
        
        // Find make('fieldName')
        if (preg_match('/::make\([\'"]([a-zA-Z0-9_\.]+)[\'"]\)/', $line, $matches)) {
            $fieldName = $matches[1];
            
            // Check if it already has ->label() on this line
            if (strpos($line, '->label(') !== false) {
                $newLines[] = $line;
                continue;
            }
            
            // Check next few lines for label
            $hasLabel = false;
            for ($j = 1; $j <= 5; $j++) {
                if (isset($lines[$i+$j]) && strpos($lines[$i+$j], '->label(') !== false) {
                    $hasLabel = true;
                    break;
                }
                if (isset($lines[$i+$j]) && preg_match('/::make\(/', $lines[$i+$j])) {
                    break; // next field started
                }
            }
            
            if (!$hasLabel && isset($dict[$fieldName])) {
                $label = str_replace("'", "\'", $dict[$fieldName]);
                // Safely inject ->label('...') directly after ::make('...')
                $searchRegex = '/(::make\([\'"]' . preg_quote($fieldName, '/') . '[\'"]\))/';
                $line = preg_replace($searchRegex, "$1->label('{$label}')", $line);
                $modified = true;
            }
        }
        
        $newLines[] = $line;
    }
    
    if ($modified) {
        file_put_contents($file, implode("\n", $newLines));
        echo "Updated $file\n";
    }
}
echo "Done!\n";
